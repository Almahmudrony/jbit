#include "nano.h"

void lcd_clear() {
	int i;
	for (i = 0; i < LCD_WIDTH * LCD_HEIGHT / 8; i++)
		lcd_write(LCD_DATA, 0);
}

void lcd_goto(int col, int row) {
	lcd_write(LCD_COMMAND, 0x80 | col);
	lcd_write(LCD_COMMAND, 0x40 | row);
}

void lcd_home() {
	lcd_goto(0, 0);
}
